# Jaki będzie wynik poniższych operacji logicznych? Sklasyfikuj operacje na koniunkcję i alternatywę logiczną.

# True and False:
# Wynikiem tej operacji jest False, ponieważ oba warunki muszą być prawdziwe, aby uzyskać True.
# W takim przypadku drugi warunek jest False.
#
# True and True:
# Wynikiem tej operacji jest True, ponieważ oba warunki muszą być prawdziwe, aby uzyskać True.
# W takim przypadku oba warunki są prawdziwe.
#
# False or False:
# Wynikiem tej operacji jest False, ponieważ przynajmniej jeden z warunków musi być prawdziwy, aby uzyskać True.
# W takim przypadku oba warunki są fałszywe.
#
# False or True:
# Wynikiem tej operacji jest True, ponieważ przynajmniej jeden z warunków musi być prawdziwy, aby uzyskać True.
# W takim przypadku drugi warunek jest prawdziwy.